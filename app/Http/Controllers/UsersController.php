    {
              //busca el id
      $users = new \App\User;
      //le doy valores a los campos
      $users->name=$request->input('usuario');
      $users->email=$request->input('mail');
      $users->password=bcrypt($request->input('psw'));
      //Guardo
      $users->save();

      return redirect()->route('users.index');
    }

